package com.java.class19;

public class ForLoopPractice {
    public static void main(String[] args) {
        int i = 1; // initializing

        while (i<=5)    { // condition
            System.out.println(i);
            i++; // increment/decrement
        }

        for (int j = 10; j>=1; j--)    {
            System.out.println(j);
        }
    }
}

package com.java.class03;
//This is our class and the class name is - IdentifierExample
public class IdentifierExample {

    public static void main(String[] args) {

        // Syntax for the variable;
        // data_type variable_name = value;
        // In the example we have;
        // data type = byte;
        // num1 = variable name;
        // value = 127
        byte num1 = 127;    // 8 bit
        short num2 = 400;   // 2 bytes
        int num3 = 808;     // 4 bytes
        long num4 = 432432; // 8 bytes
        float num5 = 64634.345f; // 4 byte
        double num6 = 6785.5673; // 8 byte
        String name = "Bakyt";
        char gender = 'M';
        boolean answer = true;

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);
        System.out.println(name);
        System.out.println(gender);
        System.out.println(answer);
    }
}
